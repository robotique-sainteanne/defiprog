def part_3(text: str, Waldo: str) -> str:
    """
    Find where is Waldo (end of sequence).

    Parameters:
        text (str): The text containing noise and Waldo.
        Waldo (int): The number sequence to find

    Returns:
        int: the position of the last digit of the sequence
    """

    ### You code goes here ###
    ### Votre code va ici ###
    
    for index, char in enumerate(text):
        for waldoIndex, waldoChar in enumerate(Waldo):
            if text[index+waldoIndex] != waldoChar:
                break
            if waldoIndex == len(Waldo)-1:
                return str(index + len(Waldo)-1)


